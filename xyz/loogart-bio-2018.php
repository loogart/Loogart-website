                        <h1>2018 Loogart Bio Video</h1>
                        <p>My name is Chris Soueidan. My mother's maiden name is Lugar. That is where my artist name <strong>Loogart</strong> comes from. I have been creating artwork and doodling since I was in kindergarten. I used to entertain people with my drawings at school, doing my classmate's art homework for money and created artwork for dirt cheap for various associations. Despite these signs that should have pushed me into an art career, I selected Engineering and landed a management job at an elevator company.</p>
                        <p>After several years of work, I was looking for a career change, but wasn't sure where I was headed until I landed on <a href="http://www.stanleychowillustration.com/" target="_blank">Stanley Chow</a>'s work on the Internet. This revolutionized the way I looked at art, since I right away realized you could get paid by clients to make art. At the end of 2011, I started messing around with modern day digital tools and started replicating Stan's artwork as practice. In 2014, I created my first <a href="./montreal-cityline-illustration.php" target="_blank">successful piece of art based off Montreal City landmarks</a> which kicked started my career. </p>
                        <p>Since then, Loogart has had the pleasure to work with awesome clients such as <a href="./starbucks-cityscape-illustration" target="_blank">Starbucks</a>, <a href="./car2go-cityline-illustration" target="_blank">car2go</a>, Grant Cardone, <a href="./yellow-pages-icons-and-illustrations" target="_blank">Yellow Pages</a>, <a href="./otis-elevator-animated-gif" target="_blank">Otis Elevator</a>, <a href="./travel-smart-canada-app" target="_blank">Government of Canada</a> on illustration, graphic design and motion graphics projects. In 2018, Loogart was featured at high profile events such as the 10X Conference and the Influence Orbis Toronto Conference. Loogart has been pointed out on social media by <a href="https://www.instagram.com/p/Bf9i8jigEwS/" target="_blank">Marc Anthony</a>, <a href="https://twitter.com/CaseyNeistat/status/933016922471436288/photo/1?ref_src=twsrc%5Etfw%7Ctwcamp%5Etweetembed%7Ctwterm%5E933016922471436288&ref_url=https%3A%2F%2Floogart.com%2Finfluence-orbis-artwork" target="_blank">Casey Neistat</a>, <a href="https://www.instagram.com/p/BQ3f57Ch3QD/" target="_blank">Russell Peters</a> and Alex Quin. It was also an honour for Loogart to give Canada's Prime Minister Justin Trudeau his very own Loogmoji in July of 2018.</p>
