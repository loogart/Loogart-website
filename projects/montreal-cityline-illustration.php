                    <div class="row">
                        <div class="col-md-6 col-md-offset-3">
                            <h3>The story behind the CityLine</h3>
                            <p>In the Summer of 2013, one of my best friends commissioned me to create a piece of art for his new apartment. The subject of this piece: Montreal City. We agreed on specific landmarks and elements that pertain to the city.</p>
                            <p> This project sat on my desk for quite some time until I decided to try something in November 2013. Within minutes, I created the center of the piece and within several hours, the full illustration was complete: thick bold lines, a minimal color palette and circular hollow anchors with a Montreal vibe.</p>
                            <p>This piece was well received by my friends and fellow Montrealers. It was also an award-nominated design during the 2014 Concours L'Expo+ and caught the attention of Montreal media.</p>
